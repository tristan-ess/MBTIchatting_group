def get_personality_list():
    """获取所有人格类型列表"""
    return list(MBTI_PROMPTS.keys())


def get_personality(mbti_type):
    """获取某个人格的完整信息"""
    return MBTI_PROMPTS.get(mbti_type.upper() if mbti_type else None)


def get_personalities_by_category():
    """按类别分组获取人格"""
    categories = {}
    for key, info in MBTI_PROMPTS.items():
        cat = info.get("category", "其他")
        if cat not in categories:
            categories[cat] = []
        categories[cat].append({"type": key, **info})
    return categories


def get_personality_system_prompt(mbti_type):
    """获取某个人格的system_prompt（便捷方法）"""
    p = get_personality(mbti_type)
    return p["system_prompt"] if p else None
